<h2><b> why credit complaint ? </b></h2>
<p>&emsp; &emsp; &emsp; The Credit Complaint is an initiative to Solve the Problem in the Society Without wasting
 a point of time in standing in the queue and avoiding bribe amoung the Government Society. So, to the Indian Society,
 we designate every indian to post their complaints to the site. And this will take it to appropriate department in the
 Government. And to the add on, they also reply you with their progress status of the project being done.So, Welcome to
 credit complaint and make the change and be the change.</p>
 <h2> Steps Involved </h2>
 <h2> Resident</h2>
 <p> &emsp; &emsp; &emsp;Register to site, If Not. Then Login and make Your complaints in the <b> Complaints </b> section present
